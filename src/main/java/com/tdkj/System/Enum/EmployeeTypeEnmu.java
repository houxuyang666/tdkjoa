public enum EmployeeTypeEnmu {
    //人员类型
    Admin(1,"超级管理员"),

    Dpartment_manager(2,"部门经理"),

    Enterprise_staff(3,"企业职员"),

    Field_staff(4,"外勤人员");


    private String desc;//文字描述

    private Integer code; //对应的代码

    /**
     * 私有构造,防止被外部调用
     * @param desc
     */
    EmployeeTypeEnmu(Integer code, String desc){
        this.desc=desc;
        this.code=code;
    }
    /**
     * 定义方法,返回描述,跟常规类的定义没区别
     * @return
     */
    public String getDesc(){
        return desc;
    }

    /**
     * 定义方法,返回代码,跟常规类的定义没区别
     * @return
     */
    public int getCode(){
        return code;
    }


}
